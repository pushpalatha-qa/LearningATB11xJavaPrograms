package ex_04_Operators;

public class Lab_040_All_Operator {
    public static void main(String[] args) {
        //Arthimetic Operator
             int a = 10;
        int b = 20;
        System.out.println(a + b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println(a - b);

        //Relational Operator - >,<,>=,<=,==,!=
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);

        // Logical Operators  -        //  OR->||  , AND->&& , NOT->!
        boolean  b1= false;
        boolean  b2= true;
        System.out.println(b1||b2);
        System.out.println(b1&&b2);
        System.out.println(b1 !=b2);
    }


    }

