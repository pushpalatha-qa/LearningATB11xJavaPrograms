public class Lab_103_For_Break {
    public static void main(String[] args) {
                for (int i = 0; i < 50; i++) {
                                  if (i == 5) {
                                       break;  //break keyword - comes out of loop - here when i reaches 5,
                    // the program stops execution ad comes out of loop
// so the output will print from 0 to 4.
                }
                    System.out.println("The value of i is " + i);
            }
            }
        }
