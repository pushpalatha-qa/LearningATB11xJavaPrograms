package ex_10_For_Loop;

public class Lab_091_For_Loop_InterviewQues {
    public static void main(String[] args) {
        for (int i = 0; i > 1 ; i++) {
            System.out.println(i);
        }
        System.out.println("End");
    }

}
//here 0 is not greater than 1 so the condition is false, so it comes out of loop.
//output will be "End".