package ex_10_For_Loop;

public class Lab_101_For_Interview_06 {
    public static void main(String[] args) {
        System.out.println("-----The loop starts from 0------");
        for (int i = 0; i <= 10; i++) { // output will print from  0 to 10, - 11 times
            System.out.println(i);
                 }
        System.out.println("-----The loop starts from 1------");
        for (int i = 1; i <= 10; i++) { // if we start from 1 , then output will print from 1 to 10, -10 times
            System.out.println(i);
        }
    }
}
